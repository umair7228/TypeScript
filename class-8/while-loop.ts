// Basic syntax of While loop
// while (condition) {
//     statement
// }

// Generate table of 5 using while loop

let i = 1;

// While loop check the condition first then execute the statement
while (i <= 10) {
    console.log(5, "x", i, "=", 5 * i);
    i++;
}



// do while
let j = 11;

// do while loop execute the statement first then check the condition 
do {
    console.log(7, "x", j, "=", 7 * j, "line 19");
    j++;
}
while (j <= 10); 
